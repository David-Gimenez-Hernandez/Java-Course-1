public class Estado{
  public static void main(String[] args){
    int edad = 18;
    boolean esConductor = true;
    double altura = 1.70;
    System.out.println("ESTADO INICIAL");
    System.out.println("--------------");
    System.out.println("edad = "+ edad);
    System.out.println("esConductor = "+ esConductor);
    System.out.println("altura = "+ altura);
    esConductor = (edad>18);
    System.out.println("-----------------");
    System.out.println("ESTADO INTERMEDIO");
    System.out.println("-----------------");
    System.out.println("edad = "+ edad);
    System.out.println("esConductor = "+ esConductor);
    System.out.println("altura = "+ altura);
    edad = 20;
    altura = altura+0.05;
    esConductor = (edad>18);
    System.out.println("------------");
    System.out.println("ESTADO FINAL");
    System.out.println("------------");
    System.out.println("edad = "+ edad);
    System.out.println("esConductor = "+ esConductor);
    System.out.println("altura = "+ altura);
  }//de main
}//de Estado